<?php

if (!function_exists('getGreeting')) {
  function getGreeting()
  {
    date_default_timezone_set('Europe/Moscow');
    $hour = (int) date('H');
    // Определяем приветствие по диапазонам времени
    if ($hour >= 5 && $hour < 10) {
      $key = 'morning';
    } elseif ($hour >= 10 && $hour < 19) {
      $key = 'day';
    } elseif ($hour >= 19 && $hour <= 23) {
      $key = 'evening';
    } else {
      $key = 'night';
    }
    $greetings = [
      'morning' => 'Доброе утро!',
      'day'     => 'Добрый день!',
      'evening' => 'Добрый вечер!',
      'night'   => 'Вам не спится? Тогда я расскажу Вам о себе.',
    ];
    return $greetings[$key];
  }
}


